package dev.jason.app;

// CLasses follow Pascal case like GetThisItem();
public class Hello {

	public static void main(String[] args) {
		System.out.println("Hello World!");
		
		
		// naming conventions
		// Variables should be lower case
		String name = "Jason Clair";
		String fname = "Jason";
		String lname = "Clair";
		
		// Snake case DO NOT USE
		String first_name = "Jason";
		
	}
	// Methods use camel case
	public static void sayHelloEveryone() {
		System.out.println("Say hello everyone!");
	}
}
